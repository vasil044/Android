public class City {
    public int id;
    public String nombre;
    public String cc;
    public String district;
    public int population;

    public City(int id, String nombre, String cc,String district,int population){
        this.id = id;
        this.nombre = nombre;
        this.cc = cc;
        this.district = district;
        this.population = population;
    }
}
